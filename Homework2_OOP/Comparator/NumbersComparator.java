
public class NumbersComparator{
    
    public void Max(double a, double b){

        if(a>b) { System.out.println("Max number is: " + a);}
    
        else { System.out.println("Max number is: " + b);}
    }
    

    public void Min(double a, double b){

    if(a<b) { System.out.println("Min number is: " + a);}

    else {System.out.println("Min number is: " + b);}
    }
}